# Syarat dan Ketentuan Layanan JasaBersih.com

Syarat dan ketentuan ini adalah kesepakatan antara pengguna ("Pelanggan") dan JasaBersih.com ("Penyedia Layanan"). Dengan melakukan pemesanan, Pelanggan menyetujui seluruh ketentuan di bawah ini.

## Peringatan keamanan

Demi keamanan data dan kejelasan tanggung jawab, Pelanggan tidak disarankan memberikan nomor HP pribadi atau melakukan transaksi langsung ke petugas cleaner.

Untuk pemesanan, perubahan jadwal, komplain, atau hal penting lainnya, harap selalu melalui kanal resmi JasaBersih.com seperti website `jasabersih.com` atau WhatsApp resmi.

Jika komunikasi atau transaksi dilakukan di luar kanal resmi, hal tersebut tidak aman dan berada di luar tanggung jawab owner atau management.

## Catatan penting

Untuk layanan paket atau langganan ada batasan durasi dan jumlah area per kunjungan, sesuai ketentuan Paket Murah Berlangganan Aja pada poin 12 di bawah.

## 1. Lingkup Layanan

JasaBersih.com menyediakan layanan kebersihan, antara lain:

- Pembersihan rumah tinggal, apartemen, dan kos
- Pembersihan kantor, ruko, dan ruang usaha
- Cuci sofa, kasur, karpet, dan gorden
- Pembersihan berat sesuai kebutuhan, misalnya pasca renovasi atau pindahan
- Jasa sterilisasi dan desinfeksi jika layanan sedang tersedia
- Layanan lain yang disepakati bersama dan tersedia di platform resmi

## 2. Proses Pemesanan

- Pemesanan dapat dilakukan melalui WhatsApp, website, atau aplikasi resmi.
- Pelanggan wajib memberikan informasi lengkap: alamat, jenis layanan, perkiraan luas atau ukuran ruangan, waktu pelaksanaan, dan kontak aktif.
- Pemesanan dianggap sah setelah ada konfirmasi jadwal dan, jika berlaku, pembayaran DP.
- Pemesanan tidak dapat dialihkan ke orang lain, kecuali dengan konfirmasi tertulis ke pihak JasaBersih.com.

## 3. Pembayaran

- Tarif layanan disesuaikan dengan jenis layanan, luas area, dan tingkat kesulitan di lokasi.
- Pembayaran dapat dilakukan via tunai, transfer bank, dan e-wallet seperti OVO, GoPay, dan lainnya.
- JasaBersih.com berhak meminta DP minimal 30% untuk layanan dengan waktu kerja lebih dari 1 hari atau berskala besar.

## 4. Pembatalan dan Reschedule

- Pembatalan 6 jam atau lebih sebelum waktu layanan tidak dikenakan biaya.
- Pembatalan kurang dari 6 jam dikenakan biaya pembatalan 25% dari total tagihan.
- Tidak hadir tanpa konfirmasi saat waktu layanan dianggap batal dan dikenakan biaya penuh.
- Reschedule diperbolehkan maksimal 1 kali dan tergantung ketersediaan jadwal.

## 5. Garansi dan Keluhan Layanan

- JasaBersih.com memberikan garansi maksimal 1 x 24 jam untuk ketidakpuasan hasil.
- Garansi hanya mencakup area yang telah dikerjakan dan belum digunakan kembali atau diubah oleh pihak lain setelah layanan.
- Komplain harus disampaikan melalui chat resmi dengan menyertakan dokumentasi foto atau video.

## 6. Kerusakan dan Kehilangan

- Pelanggan wajib menyimpan barang berharga dan elektronik di tempat aman sebelum layanan dimulai.
- Tim JasaBersih.com tidak dapat dimintai pertanggungjawaban atas kehilangan barang pribadi, kecuali ada bukti kuat dan terbukti dilakukan oleh petugas.
- Untuk kerusakan barang atau properti akibat kelalaian petugas, JasaBersih.com akan melakukan penggantian maksimal Rp 500.000 per kejadian atau melakukan perbaikan jika memungkinkan.
- Klaim kerusakan harus dilaporkan maksimal 24 jam setelah layanan diselesaikan, disertai bukti yang valid.

## 7. Keamanan dan Privasi

- Tim JasaBersih.com telah melalui proses seleksi dan pelatihan standar kerja serta etika.
- Pelanggan berhak menolak petugas jika dirasa tidak aman, dengan alasan yang jelas.
- Informasi pribadi pelanggan tidak akan disebarkan tanpa izin, kecuali diwajibkan oleh hukum.

## 8. Hak dan Kewajiban Pelanggan

### Pelanggan berhak:

- Mendapat layanan sesuai dengan kesepakatan
- Menyampaikan keluhan secara sopan dan resmi
- Mengajukan reschedule jika terjadi keadaan mendesak

### Pelanggan berkewajiban:

- Menyediakan akses ke area kerja, termasuk listrik, air, dan ventilasi
- Memberikan informasi yang jujur mengenai kondisi lokasi
- Membayar lunas layanan yang telah diberikan

## 9. Hak dan Kewajiban JasaBersih.com

### JasaBersih.com berhak:

- Menolak permintaan di luar SOP, misalnya pembersihan limbah medis atau berbahaya
- Menunda layanan jika terjadi force majeure seperti cuaca ekstrem atau listrik padam
- Memberikan blacklist terhadap pelanggan yang tidak kooperatif atau merugikan

### JasaBersih.com berkewajiban:

- Memberikan layanan yang sesuai standar kerja dan keselamatan
- Menindaklanjuti keluhan pelanggan secara profesional

## 10. Hukum yang Berlaku

- Syarat dan ketentuan ini tunduk pada hukum Republik Indonesia.
- Jika terjadi sengketa yang tidak bisa diselesaikan secara musyawarah, sengketa akan diselesaikan melalui jalur hukum di domisili hukum JasaBersih.com.

## 11. Perubahan Ketentuan

- JasaBersih.com berhak mengubah isi syarat dan ketentuan ini kapan saja dengan atau tanpa pemberitahuan sebelumnya.
- Pelanggan disarankan untuk membaca versi terbaru yang tersedia di situs atau media resmi kami.

## 12. Ketentuan Khusus Paket Murah Berlangganan Aja

### 12.1 Definisi dan Tujuan Paket

Paket Murah Berlangganan Aja adalah layanan kebersihan rutin berbasis kunjungan bulanan untuk menjaga rumah atau kamar tetap bersih dan rapi.

Paket ini bukan untuk kondisi yang sangat kotor, bekas renovasi, atau pembersihan berat yang butuh penanganan khusus.

### 12.2 Jadwal dan Kuota Kunjungan

- Jumlah kunjungan per bulan mengikuti paket yang dipilih dan disepakati saat pemesanan.
- Basic: 3x per bulan
- Standard: 5x per bulan
- Premium: 6x per bulan
- Jadwal kunjungan ditentukan berdasarkan ketersediaan slot dan konfirmasi kedua belah pihak.
- Paket bersifat berlangganan per bulan, dan kuota kunjungan yang tidak digunakan dalam bulan berjalan tidak dapat diakumulasikan ke bulan berikutnya, kecuali disepakati tertulis oleh JasaBersih.com.

### 12.3 Batas Maksimal Pekerjaan per Kunjungan

Paket memiliki batas maksimal pekerjaan agar durasi dan hasil tetap rapi. Batasan dapat berbeda sesuai kondisi lokasi, tingkat kotor, dan jumlah petugas yang datang.

- Paket Basic Rp 450.000 per bulan:
- Maksimal 2 jam per kunjungan
- Maksimal 1 kamar + 1 kamar mandi, atau 2 kamar tanpa kamar mandi

- Paket Standard Rp 750.000 per bulan:
- Maksimal 2 sampai 2,5 jam per kunjungan
- Maksimal 2 kamar + 1 kamar mandi, atau 3 kamar tanpa kamar mandi

- Paket Premium Rp 1.200.000 per bulan:
- Maksimal 3 jam per kunjungan
- Maksimal 3 kamar + 2 kamar mandi, atau 4 kamar + 1 kamar mandi

Jika area melebihi batas paket, Pelanggan dapat memilih menambah jam kerja dengan biaya tambahan, menambah kunjungan, atau menjadwalkan area sisa di kunjungan berikutnya sesuai kesepakatan.

### 12.4 Termasuk untuk Kebersihan Rutin

- Sapu dan pel lantai
- Lap debu permukaan seperti meja, lemari luar, dan rak
- Kaca atau cermin ringan jika ada
- Buang sampah ke tempat sampah utama
- Kamar mandi: sikat toilet, lantai area basah, lap wastafel dan kran
- Rapikan ringan, tidak termasuk merapikan barang pribadi yang banyak

### 12.5 Tidak Termasuk atau Add-on

- Cuci piring menumpuk atau masak-memasak
- Cuci baju atau setrika
- Mengangkat atau memindahkan furnitur berat
- Jamur parah, kerak tebal, nat hitam berat, karena termasuk pembersihan khusus
- Pasca renovasi, debu semen, bongkar gudang atau lemari, pembuangan sampah besar
- Kasur, sofa, atau karpet dengan alat khusus, kecuali dipesan terpisah

### 12.6 Khusus Kost, Hotel, Guest House, dan Villa

- Paket cocok untuk kost, hotel, guest house, dan villa karena dapat dijalankan dengan sistem turnover kamar atau kamar siap disewakan dan check-in.
- Pekerjaan turnover fokus pada lantai, kamar mandi, buang sampah, lap permukaan, dan kerapian dasar.
- Jika jumlah kamar yang dibersihkan melebihi batas paket dalam 1 kunjungan, maka akan diterapkan rotasi kamar, tambahan jam, atau tambahan kunjungan sesuai kesepakatan.

### 12.7 Kondisi Lokasi dan Penyesuaian Durasi

- Jika kondisi lokasi sangat kotor, banyak barang menumpuk, akses sulit, atau membutuhkan penanganan khusus, JasaBersih.com dapat menyesuaikan area yang dikerjakan dalam batas durasi paket dan atau menawarkan layanan tambahan.

### 12.8 Konfirmasi Fokus Area

- Sebelum mulai bekerja, Pelanggan wajib menyebutkan fokus area prioritas, misalnya kamar mandi, kamar, atau dapur.
- Jika tidak ada fokus, tim akan mengerjakan berdasarkan standar operasional umum.

Catatan add-on: pembersihan khusus, tambahan jam, atau tambahan kunjungan akan diinformasikan dan disetujui Pelanggan terlebih dahulu sebelum dikerjakan.

## 13. Keselamatan Kerja dan Batasan Layanan

- JasaBersih.com mengutamakan keselamatan tim dan Pelanggan. Tim berhak menolak atau menghentikan pekerjaan yang berisiko tinggi, seperti ketinggian tanpa alat pengaman, instalasi listrik terbuka, lantai licin ekstrem, atau area tidak aman.
- Layanan tidak mencakup penanganan limbah berbahaya, medis, atau kimia tanpa prosedur khusus dan persetujuan tertulis.
