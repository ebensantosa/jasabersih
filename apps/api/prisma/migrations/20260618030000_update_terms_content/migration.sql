- Permintaan tambahan di luar kesepakatan awal wajib dikonfirmasi terlebih dahulu agar tidak terjadi miskomunikasi atau pekerjaan tambahan tanpa persetujuan.

## 15. Kebijakan Refund dan Pembersihan Ulang

- Pelanggan tidak berhak meminta refund atau pengembalian uang setelah layanan telah dilaksanakan, kecuali dalam kondisi tertentu yang diatur dalam ketentuan garansi pada Pasal 5.
- Pembayaran yang sudah dilakukan tidak dapat dikembalikan setelah layanan dilaksanakan.
- Jika pelanggan tidak puas dengan hasil layanan, JasaBersih.com akan memberikan pembersihan ulang pada area yang telah dikerjakan dalam waktu maksimal 1 x 24 jam setelah layanan selesai, sesuai dengan ketentuan garansi yang berlaku.
- Pembersihan ulang hanya berlaku untuk area yang belum digunakan atau diubah oleh pihak lain setelah layanan.
- Jika kondisi lokasi atau permintaan pelanggan berubah setelah layanan selesai, pembersihan ulang akan dikenakan biaya tambahan sesuai ketentuan yang berlaku.
- Untuk mengajukan pembersihan ulang, Pelanggan wajib menggunakan chat resmi JasaBersih.com dengan menyertakan dokumentasi foto atau video terkait hasil yang tidak memuaskan.
- Permintaan pembersihan ulang yang diajukan lebih dari 1 x 24 jam setelah layanan selesai tidak akan diproses.

## 16. Force Majeure

- JasaBersih.com tidak bertanggung jawab atas keterlambatan atau ketidakmampuan layanan akibat keadaan di luar kendali seperti bencana alam, banjir, kerusuhan, pemadaman luas, gangguan akses jalan, dan sejenisnya.
- Dalam kondisi tersebut, jadwal dapat disesuaikan tanpa penalti, dan akan dicarikan slot pengganti terbaik.

