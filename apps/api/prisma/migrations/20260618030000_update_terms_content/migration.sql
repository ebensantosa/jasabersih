- Jika kondisi lokasi tidak memungkinkan seperti akses terkunci, tidak ada air atau listrik, atau area membahayakan, jadwal dapat dijadwalkan ulang.

## 14. Ketertiban, Etika, dan Perlindungan Tim

- Pelanggan wajib menjaga suasana kerja yang kondusif. Perilaku intimidasi, pelecehan, atau tindakan tidak sopan terhadap tim tidak diperkenankan.
- Jika terjadi hal yang mengganggu keselamatan atau kenyamanan tim, layanan dapat dihentikan. Biaya layanan tetap berlaku sesuai progres pekerjaan yang sudah dilakukan.
